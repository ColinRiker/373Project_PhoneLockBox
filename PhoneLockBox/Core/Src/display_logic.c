// draws one character using our font table
void drawChar(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg) {
    if (c < 32 || c > 90) return;
    c -= 32;
    for (uint8_t i = 0; i < 5; i++) {
        uint8_t line = font5x7[(uint8_t)c][i];
        for (uint8_t j = 0; j < 7; j++) {
            if (line & (1 << j)) {
                LCD_DrawPixel(x+i, y+j, color);
            } else {
                LCD_DrawPixel(x+i, y+j, bg);
            }
        }
    }
}

// draws a string of text starting at (x, y)
void drawText(uint16_t x, uint16_t y, const char* text, uint16_t color, uint16_t bg) {
    while (*text) {
        drawChar(x, y, *text, color, bg);
        x += 6; // move right for next char
        text++;
    }
}

// clears the screen to black
void Display_TurnOff(void) {
    LCD_FillScreen(0x0000);
}

// shows the first screen: when powered on
void Display_ShowStartupPrompt(void) {
    LCD_FillScreen(0xFFFF); // white background
    drawText(10, 40, "Press dial to power on", 0x0000, 0xFFFF);
    drawText(10, 60, "Turn dial to set time", 0x0000, 0xFFFF);
    drawText(10, 80, "Put phone in to enable locking", 0x0000, 0xFFFF);
}

// screen where user sets time â€” shows time and selected state
void Display_ShowSetTime(uint8_t minutes, uint8_t selected) {
    LCD_FillScreen(0xFFFF);
    drawText(10, 40, selected ? "> power ON" : "  power ON", 0x07E0, 0xFFFF);
    drawText(10, 60, selected ? "  lock" : "> lock", 0xF800, 0xFFFF);

    char buffer[32];
    sprintf(buffer, "time: %d min", minutes);
    drawText(10, 100, buffer, 0x0000, 0xFFFF);
}

// countdown screen
void Display_ShowCountdown(uint8_t remainingMinutes) {
    LCD_FillScreen(0xFFFF);
    drawText(10, 60, "locking...", 0x0000, 0xFFFF);
    char buffer[32];
    sprintf(buffer, "minutes left: %d", remainingMinutes);
    drawText(10, 80, buffer, 0x0000, 0xFFFF);
}

// notification screen A: options after countdown ends
void Display_ShowNotificationA(uint8_t selectedOption) {
    LCD_FillScreen(0xFFFF);
    drawText(10, 40, selectedOption == 0 ? "> i'm here!" : "  i'm here!", 0x07E0, 0xFFFF);
    drawText(10, 60, selectedOption == 1 ? "> snooze 5 min" : "  snooze 5 min", 0x07E0, 0xFFFF);
    drawText(10, 80, selectedOption == 2 ? "> notify roomie" : "  notify roomie", 0x07E0, 0xFFFF);
}

// notification screen B: if snoozed
void Display_ShowNotificationB(uint8_t selectedOption) {
    LCD_FillScreen(0xFFFF);
    drawText(10, 40, selectedOption == 0 ? "> i'm here!" : "  i'm here!", 0xF800, 0xFFFF);
    drawText(10, 60, selectedOption == 1 ? "> notify roomie" : "  notify roomie", 0xF800, 0xFFFF);
}

// screen if box is forcefully opened
void Display_ShowForceOpen(void) {
    LCD_FillScreen(0xF800); // red
    drawText(30, 120, "box forced open", 0xFFFF, 0xF800); // white on red
}

